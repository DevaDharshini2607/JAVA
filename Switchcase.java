public class Switchcase {
    public static void main(String[] args) {
        int a= 2;
        switch (a) {
            case 1:
                System.out.println("First case");
                break;
            case 2:
                System.out.println("Second case");    
                break;
            case 3:
                System.out.println("Third case");
        
            default:
                System.out.println("Fourth Case");
                break;
        }
    }
    
}
